@router.get(
    "/me/activity",
    response_model=List[AuditLogSchema],
    status_code=status.HTTP_200_OK,
    summary="View the current user's activity history",
    description=(
        "Returns the audit log entries that belong to the currently authenticated user, "
        "ordered by most recent first. This includes login attempts (successful and failed), "
        "logouts, password changes, and any VM actions the user has performed. "
        "Use the optional `action_type` query param to filter by a specific action. "
        "Use `limit` to control how many records are returned (default 50, max 200)."
    ),
)
async def get_my_activity(
    action_type: Optional[ActionType] = Query(
        None,
        description="Filter by a specific action type (e.g. login, logout, vm_create)."
    ),
    limit: int = Query(
        50,
        ge=1,
        le=200,
        description="Maximum number of records to return. Default is 50."
    ),
    current_user: User = Depends(get_current_user),
    db: AsyncSession = Depends(get_db),
):
    query = (
        select(AuditLog)
        .where(AuditLog.user_id == current_user.id)
        .order_by(AuditLog.occurred_at.desc())
        .limit(limit)
    )
    if action_type:
        query = query.where(AuditLog.action_type == action_type)

    result = await db.execute(query)
    return result.scalars().all()
